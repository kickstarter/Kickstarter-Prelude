 # Either.swift
 
 ---
 
 The `Either` type represents values with two possibilities. In particular, if `x: Either<A, B>`,
 then `x` either holds something of type `A` or something of type `B`, but never both at the 
 same time.
 
 A close cousin to `Either<A, B>` is the tuple `(A, B)`, and Swift provides first class support
 for that type.
